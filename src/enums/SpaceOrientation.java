package enums;

public enum SpaceOrientation {
    HORIZONTAL_TOP_OUTER,    // Top corridor, outer edge (faces down)
    HORIZONTAL_TOP_INNER,    // Top corridor, inner edge (faces down)
    HORIZONTAL_BOTTOM_OUTER, // Bottom corridor, outer edge (faces up)
    HORIZONTAL_BOTTOM_INNER, // Bottom corridor, inner edge (faces up)
    VERTICAL_LEFT_OUTER,     // Left corridor, outer edge (faces right)
    VERTICAL_LEFT_INNER,     // Left corridor, inner edge (faces right)
    VERTICAL_RIGHT_OUTER,    // Right corridor, outer edge (faces left)
    VERTICAL_RIGHT_INNER     // Right corridor, inner edge (faces left)
}
